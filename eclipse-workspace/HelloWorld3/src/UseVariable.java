//변수 선언

public class UseVariable {

	public static void main(String[] args) {
		int num; //변수 선언(메모리에 공간 잡는다=메모리공간 할당)
		
		num = 10; // 10을 넣는다 (할당, 대입)
		System.out.println(num);
		
		num = 20;
		System.out.println(num);
		
		num = 100;
		System.out.println(num);
		
		
		//변수 선언 = 메모리 할당이라 읽는당당당
		
	  //데이타종류 + 용량
		byte num1; //변수 num1의 선언
		int num2 =1.01; //int는 정수이므로 에러가 남
		
		double num1; //더블은 실수가 들어가네용
		
		
		
		num1 = 10; //변수 num1에 10을 저장
		
		int num2 = 20; //변수 num2 선언과 동시에 20으로 초기화
		int num3 = num1 + num2; // 두 변수 값을 대상으로 덧셈
		System.out.println(num1 + "+" + num2 + "=" + num3);
		
		
		
		
		
		
		

	}

}



