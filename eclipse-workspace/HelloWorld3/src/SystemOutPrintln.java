

public class SystemOutPrintln {

	public static void main(String[] args) 
	{
		System.out.println("Hello World"); //문자열
		System.out.println(7); //정수
		System.out.println(3.15); //실수
		System.out.println("3 +5 = " + 8); //문자열 +8
		System.out.println(3.15 + "는 실수입니다."); //실수 + 문자열
		System.out.println("3 + 5" + "의 연산 결과는 8입니다.");
		System.out.println(3 + 5); //계산
		System.out.println("3"+5); //문자열 +정수라서 이어서 출력
		
		

	}

}