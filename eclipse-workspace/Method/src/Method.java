//1. 함수 만드는 것(정의) 2.함수 써먹는 법(호출)
public class Method {

	public static void main(String[] args) {
		
		System.out.println("프로그램의 시작"); 
		hiEveryone(10);
		hiEveryone(20);
		hiEveryone(30);		
		
	}
	
	
	public static void hiEveryone(int age) {
		System.out.println("좋은 아침입니다.");
		System.out.println("제 나이는" + age + "세 입니다.");
		
		
	}
			

		


}
